const interpolate = (value, params = {}) =>
  value.replace(/\{(\w+)\}/g, (_, key) => params[key] ?? "");

export const localizeDigits = (value, language) => {
  return `${value ?? ""}`;
};

export const translate = (language, key, params) => {
  const entry = translations[language]?.[key] ?? translations.en[key] ?? key;
  let resolved = key;
  if (typeof entry === "function") {
    resolved = entry(params || {});
  } else if (typeof entry === "string") {
    resolved = interpolate(entry, params);
  } else {
    resolved = key;
  }
  return resolved;
};

export function useI18n() {
  const language = useLanguageStore((state) => state.language);

  return {
    language,
    t: (key, params) => translate(language, key, params),
    localizeDigits: (value) => localizeDigits(value, language),
    formatNumber: (value) => Number(value || 0).toLocaleString(),
  };
}
